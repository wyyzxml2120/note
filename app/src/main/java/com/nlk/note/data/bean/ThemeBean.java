package com.nlk.note.data.bean;

public class ThemeBean {
    private int path;
    private int type;
    private String tip;


    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }


    public int getPath() {
        return path;
    }

    public void setPath(int path) {
        this.path = path;
    }

    public String getTip() {
        return tip;
    }

    public void setTip(String tip) {
        this.tip = tip;
    }
}
